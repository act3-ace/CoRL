import streamlit as st

from corl.evaluation.recording.folder import FolderRecord
from corl.visualization.streamlit_app.default_layout import DefaultStreamlitPage

if __name__ == "__main__":
    st.set_page_config(layout="wide")

    # #########################################################################
    # Inspects results generated by running CoRL's separate evaluation script.
    # #########################################################################

    # #########################################################################
    # Directory Selector
    if st.session_state.get("main_page_loaded"):
        st.markdown("# Explore Custom Evaluation Results")
        if not hasattr(st.session_state, "default_app"):
            st.session_state.default_app = DefaultStreamlitPage(file_loader=FolderRecord)

        st.session_state.default_app.display_directory_picker()
        st.session_state.default_app.display_load_episode_artifact_option()
        if st.session_state.default_app.full_experiment_path and st.session_state.default_app.tables_dict:
            st.session_state.default_app.display_config_file()
            st.session_state.default_app.display_episode_metadata_section()
            st.session_state.default_app.display_agent_dones_section()
            st.session_state.default_app.display_platform_dones_section()
            st.session_state.default_app.display_initial_states_section()
            st.session_state.default_app.display_units_selector()
            st.session_state.default_app.display_agent_steps_section()
            if st.session_state.layout_config:
                st.session_state.default_app.display_trajectory_animation_section(
                    **st.session_state.layout_config["streamlit_layout"]["display_trajectory_animation_section"]
                )
            else:
                st.warning("Missing Trajectory animation configuration. No animation is provided.")
            st.session_state.default_app.display_space_data_table()
    else:
        st.warning("Main page must be loaded before navigating to subsequent pages. Navigate back to the first page `streamlit_app`)")
